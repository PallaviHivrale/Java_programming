import java.util.Scanner;

class Relational_Operators
{
    public static void main(String A[])
    {
        int p = 20, q = 15;

        System.out.println(p == q); // falsa
        System.out.println(p != q); // true
        System.out.println(p <= q); // true
        System.out.println(p >= q); // false
        System.out.println(p < q);  // true
        System.out.println(p > q);  // false
    }
}