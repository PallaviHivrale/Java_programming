import java.util.Scanner;

class Assignment_Operators
{
    public static void main(String A[])
    {
        int p = 10;
        int q;

        //  =
        q = p;
        System.out.println(q);

        // +=
        p += q;
        System.out.println(p);

        // -=
        p -= q;
        System.out.println(p);

        // +=
        p *= q;
        System.out.println(p);

        // /=
        p /= q;
        System.out.println(p);

        
    }
}